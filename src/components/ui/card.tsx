import { cn } from "@/lib/utils";
import { HTMLAttributes, forwardRef } from "react";

const Card = forwardRef<HTMLDivElement, HTMLAttributes<HTMLDivElement>>(
  ({ className, ...props }, ref) => (
    <div
      ref={ref}
      className={cn(
        "rounded-2xl border-2 border-slate-100 bg-white/80 backdrop-blur-sm shadow-lg shadow-slate-200/50 transition-all duration-300 hover:shadow-xl hover:shadow-slate-200/60 hover:border-primary-100 dark:border-slate-800 dark:bg-slate-900/80 dark:shadow-slate-900/50 dark:hover:shadow-slate-900/60 dark:hover:border-primary-900",
        className
      )}
      {...props}
    />
  )
);
Card.displayName = "Card";

